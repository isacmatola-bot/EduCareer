const translations: Record<LanguageCode, Record<string, string>> = { en, pt, jp };

const I18nContext = createContext({
  language: 'en' as LanguageCode,
  t: createTranslator('en')
});

export function normalizeLanguage(language: string | null | undefined): LanguageCode {
  return language === 'pt' || language === 'jp' || language === 'en' ? language : 'en';
}

export function languageToHtmlLang(language: LanguageCode): string {
  return language === 'jp' ? 'ja' : language;
}

export function createTranslator(language: string | null | undefined) {
  const normalized = normalizeLanguage(language);

  return function t(key: string, vars?: TranslationVars): string {
    const template = translations[normalized][key] ?? translations.en[key] ?? key;

    if (!vars) {
      return template;
    }

    return Object.entries(vars).reduce(
      (current, [name, value]) => current.split(`{${name}}`).join(String(value)),
      template
    );
  };
}

export function I18nProvider({ language, children }: { language: LanguageCode; children: ReactNode }) {
  return (
    <I18nContext.Provider value={{ language, t: createTranslator(language) }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  return useContext(I18nContext);
}

export function formatRole(role: AccountRole, t: (key: string) => string): string {
  return t(`role.${role}`);
}

export function formatAdminRole(role: AdminRole, t: (key: string) => string): string {
  return t(`adminRole.${role}`);
}

export function formatAccountLabel(account: UserAccount, t: (key: string) => string): string {
  if (account.role === 'admin' && account.adminRole) {
    return `${account.displayName} · ${formatAdminRole(account.adminRole, t)}`;
  }

  return `${account.displayName} · ${formatRole(account.role, t)}`;
}

